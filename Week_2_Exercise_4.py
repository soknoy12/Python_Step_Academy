#Operator
import math

Num_1 = 5
Num_2 = 2

print(Num_1 + Num_2)  # 7
print(Num_1 - Num_2)  # 3
print(Num_1 * Num_2)  # 10
print(Num_1 / Num_2)  # 2.5
print(Num_1 % Num_2)  # 1
print(Num_1 // Num_2) # 2
print(Num_1 ** Num_2) # 25

Num_3 = math.sqrt (16)
print(Num_3)

